// Shuffle options and adjust correct answer position
function shuffleOptions(questions) {
    questions.forEach(subject => {
        subject.forEach(q => {
            const shuffled = [...q.options].sort(() => Math.random() - 0.5);
            q.options = shuffled;
            q.correctIndex = shuffled.indexOf(q.answer);
        });
    });
}

